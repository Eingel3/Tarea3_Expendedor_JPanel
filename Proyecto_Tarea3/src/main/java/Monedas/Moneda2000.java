package Monedas;

/**
 * Clase que representa una moneda de 2000.
 */
public class Moneda2000 extends Moneda{
    /**
     * Constructor de la moneda de 2000.
     */
    public Moneda2000(){
        super();
    }

    /**
     * Obtiene el valor de la moneda.
     *
     * @return valor de la moneda
     */
    @Override public int getValor(){
        return 2000;
    }
}
